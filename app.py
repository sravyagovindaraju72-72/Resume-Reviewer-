def read_pdf(file_bytes):
    try:
        from pypdf import PdfReader
        reader = PdfReader(BytesIO(file_bytes))
        return "\n".join(p.extract_text() or "" for p in reader.pages).strip()
    except Exception:
        return ""

def read_docx(file_bytes):
    try:
        import docx
        doc = docx.Document(BytesIO(file_bytes))
        return "\n".join(p.text for p in doc.paragraphs).strip()
    except Exception:
        return ""

def clean_text(t):
    t = (t or "").replace("\x00", " ")
    return re.sub(r"\s+", " ", t).strip()

def normalize_title(title):
    title = re.sub(r"[^a-z0-9\s/&-]", " ", str(title).lower())
    noise_words = {
        "sr", "senior", "jr", "junior", "lead", "principal", "staff",
        "associate", "assistant", "intern", "ii", "iii", "iv", "i"
    }
    tokens = [tok for tok in title.split() if tok not in noise_words]
    return " ".join(tokens).strip() or str(title).strip()

def role_family_key(title):
    normalized = normalize_title(title)
    tokens = normalized.split()
    family = [tok for tok in tokens if tok in ROLE_FAMILY_WORDS]
    if family:
        return " ".join(family[:2])
    return " ".join(tokens[:2]) if tokens else normalized

def significant_terms(text, limit=12):
    tokens = re.findall(r"[a-z]{4,}", str(text).lower())
    filtered = []
    for token in tokens:
        if token in COMMON_WORDS:
            continue
        if token not in filtered:
            filtered.append(token)
        if len(filtered) == limit:
            break
    return set(filtered)

def keyword_quality(term):
    words = term.split()
    if not words:
        return False
    if len(words) > 3:
        return False
    if len(words) == 1 and words[0] in GENERIC_SINGLE_WORDS:
        return False
    if any(len(word) < 3 for word in words):
        return False
    if all(word in COMMON_WORDS for word in words):
        return False
    if len(words) == 2 and any(word in COMMON_WORDS for word in words):
        return False
    return True

def normalize_keyword_word(word):
    word = word.lower().strip()
    if word.endswith("ies") and len(word) > 4:
        return word[:-3] + "y"
    if word.endswith("s") and len(word) > 3 and not word.endswith("ss"):
        return word[:-1]
    return word

def has_redundant_words(term):
    normalized_words = [normalize_keyword_word(word) for word in term.split()]
    return len(normalized_words) != len(set(normalized_words))

def keyword_token_set(term):
    return {normalize_keyword_word(word) for word in term.split()}

def term_matches_text(term, text):
    text_lower = clean_text(text).lower()
    if term in text_lower:
        return True

    term_words = [normalize_keyword_word(word) for word in re.findall(r"[a-z0-9]+", term.lower())]
    text_words = {normalize_keyword_word(word) for word in re.findall(r"[a-z0-9]+", text_lower)}
    significant = [word for word in term_words if word not in COMMON_WORDS and word not in JOB_POSTING_NOISE]

    if not significant:
        return False
    if len(significant) == 1:
        return significant[0] in text_words

    matched = sum(1 for word in significant if word in text_words)
    return matched == len(significant)

def clean_keywords(keywords, limit=12):
    cleaned = []
    seen = set()
    for keyword in keywords:
        normalized = re.sub(r"[^a-z0-9\s/&-]", " ", str(keyword).lower())
        normalized = re.sub(r"\s+", " ", normalized).strip()
        if not normalized or normalized in seen:
            continue
        if not keyword_quality(normalized):
            continue
        if has_redundant_words(normalized):
            continue
        seen.add(normalized)
        cleaned.append(normalized)
        if len(cleaned) == limit:
            break
    return cleaned

def extract_known_skill_phrases(text):
    text_lower = clean_text(text).lower()
    found = []
    for phrase in sorted(KNOWN_SKILL_PHRASES, key=len, reverse=True):
        if phrase in text_lower and phrase not in found:
            found.append(phrase)
    return found

def keyword_specificity(term):
    words = term.split()
    score = 0
    if term in KNOWN_SKILL_PHRASES:
        score += 6
    if len(words) == 1 and len(term) <= 3:
        score += 4
    if any(char.isdigit() for char in term):
        score += 2
    if len(words) >= 2:
        score += 2
    if len(words) > 3:
        score -= 6
    if any(word not in COMMON_WORDS for word in words):
        score += 2
    if term in JOB_POSTING_NOISE:
        score -= 8
    if any(word in JOB_POSTING_NOISE for word in words):
        score -= 4
    if len(words) == 1 and words[0] in GENERIC_SINGLE_WORDS:
        score -= 8
    if any(word in MONTH_WORDS for word in words):
        score -= 8
    return score

def rank_keywords(candidates, limit):
    deduped = []
    seen = set()
    for candidate in candidates:
        normalized = re.sub(r"\s+", " ", candidate.strip().lower())
        if not normalized or normalized in seen:
            continue
        if has_redundant_words(normalized):
            continue
        seen.add(normalized)
        deduped.append(normalized)
    ranked = sorted(deduped, key=lambda term: (keyword_specificity(term), -len(term)), reverse=True)
    filtered = [term for term in ranked if keyword_specificity(term) > 0]

    selected = []
    selected_sets = []
    for term in filtered:
        term_set = keyword_token_set(term)
        skip = False
        for existing, existing_set in zip(selected, selected_sets):
            overlap_ratio = len(term_set & existing_set) / max(1, len(term_set))
            if overlap_ratio >= 0.8:
                skip = True
                break
            if term_set <= existing_set:
                skip = True
                break
            if existing_set < term_set and keyword_specificity(term) >= keyword_specificity(existing):
                idx = selected.index(existing)
                selected[idx] = term
                selected_sets[idx] = term_set
                skip = True
                break
        if skip:
            continue
        selected.append(term)
        selected_sets.append(term_set)
        if len(selected) == limit:
            break

    return selected

def is_structured_skill_line(line):
    line_lower = line.lower()
    if line_lower in JOB_POSTING_NOISE:
        return False
    if len(line.split()) > 18:
        return False
    return any(token in line_lower for token in [",", ":", "(", ")", " and ", "/"]) or any(
        phrase in line_lower for phrase in KNOWN_SKILL_PHRASES
    )

def extract_structured_terms(job_text):
    candidates = []
    for raw_line in str(job_text).splitlines():
        line = clean_text(raw_line)
        if not line or not is_structured_skill_line(line):
            continue
        lowered = line.lower()
        for phrase in extract_known_skill_phrases(line):
            if phrase not in candidates:
                candidates.append(phrase)
        for chunk in re.split(r"[:,;/()]|\band\b", lowered):
            chunk = clean_text(chunk.lower())
            if not chunk:
                continue
            if chunk in JOB_POSTING_NOISE:
                continue
            if any(word in MONTH_WORDS for word in chunk.split()):
                continue
            if len(chunk.split()) <= 3 and keyword_quality(chunk):
                candidates.append(chunk)
    return candidates

def sanitize_job_text(job_text):
    lines = []
    for raw_line in str(job_text).splitlines():
        line = clean_text(raw_line)
        if not line:
            continue
        if line.lower() in JOB_POSTING_NOISE:
            continue
        if line.endswith(":") and len(line.split()) <= 3:
            continue
        lines.append(line)
    return "\n".join(lines)

def distinct_phrases_from_text(text, limit=5):
    sentences = re.split(r"(?<=[.!?])\s+", clean_text(text))
    phrases = []
    for sentence in sentences:
        sentence = sentence.strip(" -")
        if len(sentence.split()) < 6:
            continue
        compact = re.sub(r"\s+", " ", sentence)
        if compact and compact not in phrases:
            phrases.append(compact)
        if len(phrases) == limit:
            break
    return phrases

def extract_resume_bullets(resume_text, limit=6):
    bullets = []
    for line in str(resume_text).splitlines():
        line = clean_text(line)
        if not line:
            continue
        if line.startswith(("-", "•", "*")):
            line = clean_text(line[1:])
        if len(line.split()) < 6:
            continue
        bullets.append(line)
        if len(bullets) == limit:
            break
    return bullets

def build_summary_suggestion(title, present, missing):
    title_text = normalize_title(title).title() if title and title != "Target Role" else "Professional"
    anchors = present[:2] + missing[:2]
    anchors = [term for idx, term in enumerate(anchors) if term and term not in anchors[:idx]]
    if not anchors:
        return f"{title_text} with experience delivering measurable results in cross-functional environments."
    if len(anchors) == 1:
        skills_text = anchors[0]
    elif len(anchors) == 2:
        skills_text = f"{anchors[0]} and {anchors[1]}"
    else:
        skills_text = ", ".join(anchors[:-1]) + f", and {anchors[-1]}"
    return f"{title_text} with experience in {skills_text}, delivering measurable results across technical and cross-functional work."

def build_bullet_rewrites(resume_text, present, missing, limit=3):
    source_bullets = extract_resume_bullets(resume_text, limit=limit * 2)
    anchor_terms = present[:2] + missing[:4]
    anchor_terms = [term for idx, term in enumerate(anchor_terms) if term and term not in anchor_terms[:idx]]
    rewrites = []

    for bullet in source_bullets:
        bullet_lower = bullet.lower()
        bullet_terms = [term for term in anchor_terms if any(word in bullet_lower for word in term.split())]
        selected_terms = bullet_terms[:2] if bullet_terms else anchor_terms[:2]
        if not selected_terms:
            continue
        words = bullet.split()
        base = " ".join(words[:16]) + ("..." if len(words) > 16 else "")
        if len(selected_terms) == 1:
            after = f"Reframed to emphasize **{selected_terms[0]}** while keeping the original accomplishment and measurable impact."
        else:
            after = f"Reframed to emphasize **{selected_terms[0]}** and **{selected_terms[1]}** while keeping the original accomplishment and measurable impact."
        rewrites.append({"before": bullet, "after": f"{base} {after}"})
        if len(rewrites) == limit:
            break

    if not rewrites and anchor_terms:
        rewrites.append({
            "before": "Generic experience bullet",
            "after": f"Lead with **{anchor_terms[0]}**, describe the action you took, and end with the measurable result."
        })
    return rewrites

def role_summary(row):
    title = row.get("title", "This role")
    desc_phrases = distinct_phrases_from_text(row.get("description", ""), limit=1)
    if desc_phrases:
        sentence = desc_phrases[0]
        words = sentence.split()
        summary = " ".join(words[:24]) + ("..." if len(words) > 24 else "")
        return f"{title} focuses on {summary[0].lower() + summary[1:]}" if summary else title
    skills = clean_keywords(re.split(r"[,\n;/]+", row.get("skills", "")), limit=4)
    if skills:
        return f"{title} centers on {', '.join(skills)}."
    return f"{title} is one of the closest roles in the dataset for this resume."

def summarize_skills(skills_text, limit=5):
    raw_skills = re.split(r"[,\n;/]+", str(skills_text))
    cleaned = clean_keywords(raw_skills, limit=limit)
