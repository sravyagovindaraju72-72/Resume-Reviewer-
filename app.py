# ─── Keyword analysis ─────────────────────────────────────────────────────────
def top_keywords(job_text, resume_text, top_k=30):
    try:
        cleaned_job_text = sanitize_job_text(job_text)
        vec = TfidfVectorizer(stop_words="english", ngram_range=(1, 3), min_df=1)
        X = vec.fit_transform([cleaned_job_text, resume_text])
        terms = np.array(vec.get_feature_names_out())
        job_scores = X[0].toarray().ravel()
        top_idx = np.argsort(job_scores)[::-1][:top_k * 4]
        tfidf_terms = clean_keywords(terms[top_idx], limit=top_k * 4)
        explicit_terms = extract_known_skill_phrases(cleaned_job_text)
        structured_terms = extract_structured_terms(job_text)
        kw = rank_keywords(explicit_terms + structured_terms + tfidf_terms, limit=top_k)
        present = [k for k in kw if term_matches_text(k, resume_text) and k not in JOB_POSTING_NOISE]
        missing = [k for k in kw if not term_matches_text(k, resume_text) and k not in JOB_POSTING_NOISE]
        return present, missing
    except Exception:
        return [], []

# ─── Core scoring ─────────────────────────────────────────────────────────────
def score_resume_vs_job(resume_text, job_text, model):
    def chunk_text(text, size=150, overlap=30):
        words = text.split()
        if len(words) <= size:
            return [text]
        chunks = []
        i = 0
        while i < len(words):
            chunks.append(" ".join(words[i:i + size]))
            i += size - overlap
        return chunks

    resume_chunks = chunk_text(resume_text)
    job_chunks = chunk_text(job_text)

    all_texts = resume_chunks + job_chunks
    all_embs = model.encode(
        all_texts,
        normalize_embeddings=True,
        convert_to_numpy=True,
        show_progress_bar=False
    )

    r_embs = all_embs[:len(resume_chunks)]
    j_embs = all_embs[len(resume_chunks):]

    sim_matrix = cosine_similarity(j_embs, r_embs)
    best_per_job = sim_matrix.max(axis=1)
    embedding_score = float(best_per_job.mean())

    present, missing = top_keywords(job_text, resume_text, top_k=30)
    total = len(present) + len(missing)
    keyword_score = len(present) / total if total > 0 else 0.0

    final_score = (0.65 * embedding_score) + (0.35 * keyword_score)
    return final_score, present, missing

